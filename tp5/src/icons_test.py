# Define una función para graficar una matriz
def graficar_matriz(matriz, titulo):
    plt.figure(figsize=(6, 6))
    plt.imshow(matriz, cmap='gray', interpolation='nearest')
    plt.title(titulo)
    plt.axis('off')
    plt.show()

